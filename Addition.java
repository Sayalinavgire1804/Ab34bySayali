package JavaClass;

public class Addition {
	static void add()
	{
		int a=100;
		int b=200;
		int c=a+b;
		System.out.println(c);
	}
	static void subtract()
	{
		int a=100;
		int b=200;
		int c=a-b;
		System.out.println(c);
		
	}
	static void multiplication() {
		int a=100;
		int b=200;
		int c=a*b;
		System.out.println(c);
	}
	static void divide()
	{
		int a=100;
		int b=200;
		int c=b/a;
		System.out.println(c);
	}

	public static void main(String[] args) {
		add();
		subtract();
		multiplication();
		divide();
		
		

	}

}
